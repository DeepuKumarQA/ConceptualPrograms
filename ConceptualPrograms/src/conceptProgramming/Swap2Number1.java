package conceptProgramming;

public class Swap2Number1 {

	public static void main(String[] args) {
		// swap with three variables
		int a = 10, b = 20, c;
		//Swapping of the values 
		c = a;
		a = b;
		b = c;
		System.out.println("The swap of 2 number using three variable: " + a + " and " + b);

	}

}
